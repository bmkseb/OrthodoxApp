/**
 * Fetches YouTube view/like counts for catalog hymns and writes data/mezmurYoutubeStats.ts.
 * Run: node scripts/build-mezmur-youtube-stats.mjs
 */
import { createClient } from '@supabase/supabase-js';
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const OUT_FILE = path.join(ROOT, 'data/mezmurYoutubeStats.ts');

function loadEnv() {
  const p = path.join(ROOT, '.env');
  if (!fs.existsSync(p)) return;
  for (const line of fs.readFileSync(p, 'utf8').split('\n')) {
    const t = line.trim();
    if (!t || t.startsWith('#')) continue;
    const eq = t.indexOf('=');
    if (eq < 0) continue;
    const k = t.slice(0, eq).trim();
    let v = t.slice(eq + 1).trim();
    if ((v.startsWith('"') && v.endsWith('"')) || (v.startsWith("'") && v.endsWith("'"))) {
      v = v.slice(1, -1);
    }
    if (!process.env[k]) process.env[k] = v;
  }
}
loadEnv();

const YOUTUBE_API_KEY = process.env.YOUTUBE_API_KEY;
const SUPABASE_URL = process.env.SUPABASE_URL;
const SUPABASE_SERVICE_ROLE_KEY = process.env.SUPABASE_SERVICE_ROLE_KEY;

if (!YOUTUBE_API_KEY) {
  console.error('Missing YOUTUBE_API_KEY in .env');
  process.exit(1);
}

async function ytFetchStatistics(videoIds) {
  const url = new URL('https://www.googleapis.com/youtube/v3/videos');
  url.searchParams.set('part', 'statistics');
  url.searchParams.set('id', videoIds.join(','));
  url.searchParams.set('key', YOUTUBE_API_KEY);

  const res = await fetch(url.toString());
  if (!res.ok) {
    const err = await res.json().catch(() => ({}));
    throw new Error(`YouTube API: ${err.error?.message ?? res.status}`);
  }
  return res.json();
}

function parseCount(value) {
  const n = Number.parseInt(String(value ?? '0'), 10);
  return Number.isFinite(n) && n >= 0 ? n : 0;
}

async function fetchCatalogVideoIds() {
  if (!SUPABASE_URL || !SUPABASE_SERVICE_ROLE_KEY) {
    console.warn('Supabase env missing — using fallback hymn IDs only.');
    return [];
  }

  const sb = createClient(SUPABASE_URL, SUPABASE_SERVICE_ROLE_KEY);
  const ids = new Set();
  let from = 0;
  const pageSize = 1000;

  while (true) {
    const { data, error } = await sb
      .from('mezmur')
      .select('video_id, type')
      .range(from, from + pageSize - 1);

    if (error) throw error;
    for (const row of data ?? []) {
      if (row.type === 'sermon') continue;
      const id = row.video_id?.trim();
      if (id) ids.add(id);
    }
    if (!data || data.length < pageSize) break;
    from += pageSize;
  }

  return [...ids];
}

async function main() {
  const fallbackIds = [
    'CIGBJy_B3VI',
    'HTihxcq1INg',
    'aLQqecKcAMQ',
    'DMHZqD4ipwE',
    'sS_Uolm6VBA',
    'xGKMCgPhwjQ',
    'ohhn3OKkUd8',
    '3V5zhgW2bes',
    '6YEPHypqfcM',
    'uD4n4ih7mac',
  ];

  const catalogIds = await fetchCatalogVideoIds();
  const videoIds = [...new Set([...catalogIds, ...fallbackIds])];
  console.log(`Fetching YouTube stats for ${videoIds.length} videos…`);

  const stats = {};
  const batchSize = 50;
  for (let i = 0; i < videoIds.length; i += batchSize) {
    const batch = videoIds.slice(i, i + batchSize);
    const data = await ytFetchStatistics(batch);
    for (const item of data.items ?? []) {
      const id = item.id?.trim();
      if (!id) continue;
      stats[id] = {
        viewCount: parseCount(item.statistics?.viewCount),
        likeCount: parseCount(item.statistics?.likeCount),
      };
    }
    console.log(`  ${Math.min(i + batchSize, videoIds.length)} / ${videoIds.length}`);
  }

  const lines = [
    '/** Auto-generated by `node scripts/build-mezmur-youtube-stats.mjs` — do not edit by hand. */',
    'export const MEZMUR_YOUTUBE_STATS: Record<string, { viewCount: number; likeCount: number }> = {',
  ];

  const sortedIds = Object.keys(stats).sort();
  for (const id of sortedIds) {
    const { viewCount, likeCount } = stats[id];
    lines.push(`  '${id}': { viewCount: ${viewCount}, likeCount: ${likeCount} },`);
  }
  lines.push('};', '');

  fs.writeFileSync(OUT_FILE, lines.join('\n'), 'utf8');
  console.log(`Wrote ${sortedIds.length} entries to data/mezmurYoutubeStats.ts`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
